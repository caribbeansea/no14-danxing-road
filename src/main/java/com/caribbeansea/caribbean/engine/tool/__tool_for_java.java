public class __tool_for_java
{

    public static void __double_for__(int a, int b, __double_for_achieve achieve)
    {
        for (int i = 0; i < a; i++)
        {
            for (int j = 0; j < b; j++)
            {
                achieve.achieve(i, j);
            }
        }
    }

}
